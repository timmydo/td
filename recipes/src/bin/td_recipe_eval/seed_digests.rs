//! The COMPILED seed-digest table (re #469): every admissible seed input's
//! expected store basename, compiled into both td-recipe-eval and td-builder
//! from the same audited repo file.
//!
//! `seed/seed-digests.txt` maps each classified seed KEY to the store
//! basename (`<hash>-<name>`) its pinned bytes derive — the content address
//! recorded when the seed was last legitimately derived. The runner enforces
//! it after EVERY derivation (`ensure_seed_input`): a derivation the table
//! does not pin, or one deriving a different basename, reds at planning. This
//! closes the two channels hash self-consistency alone leaves open:
//!
//! - a seed whose artifact is GENERATED rather than fetched (the kernel
//!   headers tarball, produced by host make/gcc) gets a compiled expected
//!   digest, so host-toolchain drift or tampering in the generated bytes
//!   reds instead of interning;
//! - `td-builder build-plan --auto` checks the SAME compiled table, so a
//!   forged MAP/SEED-STORE/SEED-DB triple — internally consistent but never
//!   derived from the pins — reds at lock synthesis even when td-builder is
//!   invoked directly (the authority is compiled in, not caller-supplied).
//!
//! Regenerate with `td-recipe-eval seed-digests > seed/seed-digests.txt`
//! (warm source cache required) whenever a pin, a seed patch, or the stage0
//! source changes; the coverage test and the ladder's first run both red
//! until the committed table agrees.

const TABLE: &str = include_str!("../../../../seed/seed-digests.txt");

/// Parse a seed-digests table: `key basename` per line, `#` comments. Any
/// malformed line is a hard error — the table is a trust anchor, never
/// best-effort.
pub(crate) fn parse(text: &str) -> Result<Vec<(&str, &str)>, String> {
    let mut rows = Vec::new();
    for (n, line) in text.lines().enumerate() {
        let line = line.trim();
        if line.is_empty() || line.starts_with('#') {
            continue;
        }
        let mut it = line.split_whitespace();
        match (it.next(), it.next(), it.next()) {
            (Some(key), Some(base), None) if !base.contains('/') => {
                // A duplicate key would let `expected()` silently vouch by the FIRST row
                // while a second, disagreeing row sat unused — an ambiguous trust anchor.
                // The table pins each seed exactly once; a repeat is a hard error (re #469).
                if rows.iter().any(|(k, _)| *k == key) {
                    return Err(format!(
                        "seed/seed-digests.txt line {}: duplicate key `{key}' — the \
                         trust-anchor table must pin each seed exactly once (re #469)",
                        n + 1
                    ));
                }
                rows.push((key, base));
            }
            _ => {
                return Err(format!(
                    "seed/seed-digests.txt line {}: malformed row `{line}' (want `key basename')",
                    n + 1
                ))
            }
        }
    }
    Ok(rows)
}

/// The compiled rows.
pub(crate) fn rows() -> Result<Vec<(&'static str, &'static str)>, String> {
    parse(TABLE)
}

/// The compiled expected basename for a seed key, if pinned.
pub(crate) fn expected(key: &str) -> Result<Option<&'static str>, String> {
    Ok(rows()?
        .into_iter()
        .find(|(k, _)| *k == key)
        .map(|(_, b)| b))
}

/// Enforce the table against a freshly DERIVED basename: the key must be
/// pinned and the derivation must reproduce the pinned basename. Reds with a
/// `provenance rejected` planning error otherwise (re #469).
pub(crate) fn require(key: &str, derived_basename: &str) -> Result<(), String> {
    match expected(key)? {
        None => Err(format!(
            "provenance rejected: seed `{key}' has no compiled expected digest in \
             seed/seed-digests.txt — an unpinned seed is not admissible; regenerate the \
             table with `td-recipe-eval seed-digests' and commit it (re #469)"
        )),
        Some(exp) if exp != derived_basename => Err(format!(
            "provenance rejected: seed `{key}' derives {derived_basename} but the compiled \
             table pins {exp} — the bytes are not the audited seed (host-generated drift, \
             tampering, or a pin bump without regenerating seed/seed-digests.txt; \
             re-derive with `td-recipe-eval seed-digests' and commit, re #469)"
        )),
        Some(_) => Ok(()),
    }
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn parse_accepts_rows_and_rejects_garbage() {
        let rows = parse("# c\nk1 aaa-x\n\nk2 bbb-y\n").unwrap();
        assert_eq!(rows, vec![("k1", "aaa-x"), ("k2", "bbb-y")]);
        assert!(parse("k1\n").is_err(), "missing basename must red");
        assert!(parse("k1 a b\n").is_err(), "extra field must red");
        assert!(parse("k1 /td/store/aaa-x\n").is_err(), "a path is not a basename");
        assert!(parse("k1 aaa-x\nk1 bbb-y\n").is_err(), "a duplicate key must red");
    }

    #[test]
    fn compiled_table_parses_and_pins_the_special_seeds() {
        let rows = rows().unwrap();
        assert!(!rows.is_empty(), "compiled table must not be empty");
        for key in ["stage0-source", "linux-headers", "linux-headers-x86-64"] {
            assert!(
                rows.iter().any(|(k, _)| *k == key),
                "compiled table must pin `{key}'"
            );
        }
    }

    #[test]
    fn require_reds_unpinned_and_mismatched_derivations() {
        let err = require("no-such-seed-key", "aaa-x").unwrap_err();
        assert!(err.contains("provenance rejected"), "{err}");
        assert!(err.contains("no compiled expected digest"), "{err}");
        let some_key = rows().unwrap().first().map(|(k, _)| *k).unwrap_or("k");
        let err = require(some_key, "0000000000000000000000000000000-not-it").unwrap_err();
        assert!(err.contains("provenance rejected"), "{err}");
        assert!(err.contains("the compiled table pins"), "{err}");
    }
}
